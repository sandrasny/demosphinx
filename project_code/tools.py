def add(var1, var2):
    """
    This function adds two integer values.
    
    Parameters:
        var1 (int): any integer value
        var2 (int): any integer value
        
    Returns:
        ans (int): the sum of the two input values
    
    """
    ans = var1 + var2
    return ans
    
def subtract(var1, var2):
    """
    This function subtracts two integer values.
    
    var2 is subtracted from var1
    
    Parameters:
        var1 (int): any integer value
        var2 (int): any integer value
        
    Returns:
        ans (int): the difference of the two input values
    
    """
    ans = var1 - var2
    return ans
        
