import numpy
import datetime

def multiply(var1, var2):
    """
    This function multiplies two integer values by each other.
    
    Parameters:
        var1 (int): any integer value
        var2 (int): any integer value
        
    Returns:
        ans (int): the product of the two input values
    
    """
    ans = var1 * var2
    return ans
    
def divide(var1, var2):
    """
    This function divides two integer values by each other.
    
    Parameters:
        var1 (int): any integer value
        var2 (int): any integer value
        
    Returns:
        ans (int): the quotient of the two input values
    
    """
    ans = var1 / var2
    return ans    
    